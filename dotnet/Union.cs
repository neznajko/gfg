public class Solution {
    public List<int> findUnion( int[] arr, int[] brr, int n, 
                                int m ){
        var set = new HashSet<int> ();
        int i = 0;
        int j = 0;
        while( i < n && j < m ){
            if( arr[ i ] <= brr[ j ]){
                set.Add( arr[ i++ ]);
            } else {
                set.Add( brr[ j++ ]);
            }
        }
        for(; i < n; ++i ){
            set.Add( arr[i] );
        }
        for(; j < m; ++j ){
            set.Add( brr[j] );
        }
        return new List<int>( set );
    }
}
