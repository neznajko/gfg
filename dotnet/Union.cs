// Union of Two Sorted Arrays
////////////////////////////////////////////////////////////////
// Union of two arrays can be defined as the common and distinct 
// elements in the two arrays. Given two sorted arrays of size n
// and m respectively, find their union.
////////////////////////////////////////////////////////////////
// Input: n = 5, arr[] = {2,2,3,4,5} m = 5, brr[] = {1,1,2,3,4}
// Output: 1 2 3 4 5
// Explanation: Distinct elements including both the arrays are:
// 1 2 3 4 5
////////////////////////////////////////////////////////////////
// Expected Time Complexity: O(n+m)
// Expected Auxiliary Space: O(n+m)
////////////////////////////////////////////////////////////////
// 1 <= n, m <= 10^5
// 1 <= arr[i], brr[i] <= 10^6
