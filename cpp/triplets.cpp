class Solution {
    vector <int> a;
    bool ck( int m ) const {
        int l = 0;
        int u = a.size() - 1;
        while( l < u ){
            int s = a[ l ] + a[ m ] + a[ u ];
            if( s == 0 ) return true;
            if( s < 0 ){
                if( ++l == m ) ++l;
            } else {
                if( --u == m ) --u;
            }
        }
        return false;
    }
    bool triplets() {
        sort( a.begin(), a.end());
        for( int m = a.size() - 2; m > 0; --m ){
            if( ck( m )) return true;
        }
        return false;
    }
public:
    bool findTriplets( vector <int> &a ){
        this->a = move( a );
        return triplets();
    }
};
