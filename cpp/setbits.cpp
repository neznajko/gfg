// Count Total Setbits
////////////////////////////////////////////////////////////////
// You are given a number N. Find the total number of setbits in
// the numbers from 1 to N.
////////////////////////////////////////////////////////////////
// Input: N = 3
// Output: 4
// Explaination: 1 -> 01, 2 -> 10 and 3 -> 11. Total 4 setbits.
////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////
// Expected Time Complexity: O(1)
// Expected Auxiliary Space: O(1)
////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////
typedef long long long_t;
////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////
inline long_t f( long_t n ){
    return n * ( 1 << ( n - 1 ));
}
////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////
long_t Solution::countBits( long_t n ){
    int off{ 0 };
    long_t mask{ 1 };
    long_t lastnum{ n & mask };
    long_t count{ lastnum };
    while( lastnum != n ){
        ++off;
        mask |= ( 1 << off );
        long_t num{ n & mask };
        if( num == lastnum ) continue;
        count += f( off ) + lastnum + 1;
        lastnum = num;
    }
    return count;
}
