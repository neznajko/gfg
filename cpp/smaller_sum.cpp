vector<i64> Solution::smallerSum( int n, const vector<int>& a ){
    vector<i64> b( n, 0 );
    auto off = getoff( n, a );
    i64 sum = a[ off[ 0 ]];
    for( int i = 1; i < n; ++i ){
        int j = off[ i ];
        int k = off[ i-1 ];
        if( a[ j ] == a[ k ]){
            b[ j ] =  b[ k ];
        } else {
            b[ j ] = sum;
        }
        sum += a[ j ];
    }
    return b;
}
