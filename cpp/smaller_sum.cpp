vector<int> getoff( int n, const vector<int>& a ){
    vector<int> off( n );
    iota( begin( off ), end( off ), 0 );
    sort( begin( off ), end( off ), [ &a ]( int i, int j ){
        return a[i] < a[j];
    });
    return off;
}
