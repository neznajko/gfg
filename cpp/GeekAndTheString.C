// Geeks And The String//////////////////////////////////////
// Our geek loves to play with strings, Currently, he is/////
// trying to reduce the size of a string by recursively//////
// removing all the consecutive duplicate pairs. In other////
// words, He can apply the below operations any number of////
// times.////////////////////////////////////////////////////
// Remove all the consecutive duplicate pairs and concatenate
// the remaining string to replace the original string.//////
// Your task is to find the string with minimum length///////
// after applying the above operations. Note: If the string//
// length become zero after applying operations, return "-1"/
// as a string.//////////////////////////////////////////////
