// Pascal Triangle
////////////////////////////////////////////////////////////////
// Given a positive integer N, return the Nth row of Pascal's //
// triangle. Pascal's triangle is a triangular array of the ////
// binomial coefficients formed by summing up the elements of //
// previous row. The elements can be large so return it modulo /
// 10^9 + 7 ////////////////////////////////////////////////////
// Input: N = 4 Output: 1 3 3 1 Explanation: 1
//                                         1 2 1  
// ( It should be 3 not 4 :)              1 3 3 1
////////////////////////////////////////////////////////////////
// Expected Time Complexity: O(N^2)
// Expected Auxiliary Space: O(N^2)
////////////////////////////////////////////////////////////////
// 1 ≤ N ≤ 10^3
////////////////////////////////////////////////////////////////
# include <iostream>
# include <vector>
////////////////////////////////////////////////////////////////
using namespace std;
////////////////////////////////////////////////////////////////
typedef long long i64;
////////////////////////////////////////////////////////////////
class Cache {
public:
    const int siz;
    vector <vector <i64>> bufr;
    int T = 1; // Top of the cache
    Cache( int siz )
        : bufr( siz, vector <i64> ( siz, 1 )),
          siz( siz )
    {}
    void debug() {
        for( int i = 0; i < siz; ++i ){
            for( int j = 0; j < siz; ++j ){
                cout << bufr[ i ][ j ] << " ";
            }
            cout << endl;
        }
    }
};
////////////////////////////////////////////////////////////////
# define MXN 1000
# define MOD 1000000007
////////////////////////////////////////////////////////////////
Cache cache( MXN + 1 );
////////////////////////////////////////////////////////////////
class Solution {
public:
    vector <i64> nthRowOfPascalTriangle( int n ){
        --n;
        while( cache.T < n ){
            int i{ cache.T };
            int j{ 1 };
            do {
                cache.bufr[ i ][ j ] =
                    cache.bufr[ i ][ j - 1 ] +
                    cache.bufr[ i - 1 ][ j ];
                cache.bufr[ i ][ j ] %= MOD;
                --i;
                ++j;
            } while( i > 0 );
            ++cache.T;
        }
        vector <i64> res;
        int i = n;
        int j = 0;
        while( i >= 0 ){
            res.push_back( cache.bufr[ i-- ][ j++ ]);
        }
        return res;
    }
};
