// Remove K Digits
////////////////////////////////////////////////////////////////
// Given a non-negative integer S represented as a string,
// remove K digits from the number so that the new number is the
// smallest possible.
// Note: The given num does not contain any leading zeroes.
////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////
// Input: S = "149811", K = 3
// Output: 111
// Explanation: Remove the three digits 4, 9, and 8 to form the
// new number 111 which is smallest
////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////
// Input: S = "1002991", K = 3
// Output: 21
// Explanation: Remove the three digits 1 (leading one), 9, and
// 9 to form the new number 21 (Note that the output must not
// contain leading zeroes) which is the smallest
////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////
// Expected Time Complexity: O(|S|)
// Expected Auxiliary Space: O(|S|)
////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////
// 1 <= K <= |S| <= 10^6
////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////
# include <iostream>
# include <string>
# include <vector>
# include <algorithm>
