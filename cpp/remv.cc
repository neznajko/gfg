class Solution {
public:
    string removeKdigits( const string& s, int k );
};
////////////////////////////////////////////////////////////////
template <typename T>
ostream& operator <<( ostream& os, const vector <T> & vec ){
    os << "[ ";
    for( const T& value: vec ){
        os << value << " ";
    }
    return os << "]";
}
////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////
string Solution::removeKdigits( const string& s, int k )
{
    int n = s.length();
    int stksiz = n - k; // vithout sentinel
    vector <char> stk( stksiz + 1, '/' );
    for( int c = stksiz - 1; c >= 0; --c ){
        stk[ c ] = s[ --n ];
    }
    while( n > 0 ){
        char digit = s[ --n ];
        int j = 0;
        while( digit <= stk[ j ]){
            swap( digit, stk[ j ]);
            ++j;
        }
    }
    string t;
    int j = 0;
    for(; stk[ j ] == '0'; ++j )
        ;
    for(; j < stksiz; ++j ){
        t.push_back( stk[ j ]);
    }
    return t == "" ? "0" : t;
}
