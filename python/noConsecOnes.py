# Consecutive 1's not allowed
################################################################
# Given a positive integer N, count all possible distinct binary
# strings of length N such that there are no consecutive 1’s.
# Output your answer modulo 10^9+7
################################################################
# Input: N = 3 Output: 5 Explanation:  5 strings are ( 000, 001,
# 010, 100, 101 )
################################################################
# Expected Time Complexity: O(N)
# Expected Auxiliary Space: O(N)
################################################################
# 1 ≤ N ≤ 10^5
################################################################
MAXN = 100000
MOD = 1000000007
COUNT = [ 0 ] * ( MAXN + 1 )
COUNT[ 0 ] = 1
COUNT[ 1 ] = 2
T = 2
################################################################
class Solution:
    def countStrings( self, n ):
        global T
        while n >= T:
            COUNT[ T ] = COUNT[ T - 1 ] + COUNT[ T - 2 ]
            COUNT[ T ] %= MOD
            T += 1
        return COUNT[ n ]
