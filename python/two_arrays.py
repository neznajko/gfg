# Counting elements in two arrays
################################################################
################################################################
# Given two unsorted arrays arr1[] and arr2[]. They may contain 
# duplicates. For each element in arr1[] count elements less
# than or equal to it in array arr2[]
################################################################
################################################################
# Input: m = 6, n = 6
#        arr1[] = {1,2,3,4,7,9}
#        arr2[] = {0,1,2,1,1,4}
# Output: 4 5 5 6 6 6
# Explanation: Number of elements less than or equal to
# 1, 2, 3, 4, 7, and 9 in the second array are respectively
# 4, 5, 5, 6, 6, 6
################################################################
################################################################
################################################################
################################################################
# Input: m = 5, n = 7
#        arr1[] = {4,8,7,5,1}
#        arr2[] = {4,48,3,0,1,1,5}
# Output: 5 6 6 6 3
# Explanation: Number of elements less than or equal to 
# 4, 8, 7, 5, and 1 in the second array are respectively
# 5, 6, 6, 6, 3
################################################################
################################################################
# Expected Time Complexity: O(MlogN+NlogN)
# Expected Auxiliary Space: O(m)
################################################################
################################################################
# 1 <= m,n <= 10^5
# 0 <= arr1[i],arr2[j] <= 10^5
################################################################
# > 馬３四（飛、桂２）
# < 玉３二、歩３一、角１二、歩１三、金５二
################################################################
# １．飛３三打　玉２一　 # ３．。。。。　玉２一 #
# ２．馬１二　　玉１二　 # ４．角１二打　玉２二 #
# ３．桂２四打　玉２二　 # ５．角２三成　玉２一 #
# ４．角１一打　玉１一　 # ６．馬１二詰　　　　 #
# ５．飛１三成　銀１二打 # 　　　　　　　　　　 #
# ６．龍１二詰　　　　　 # 　　　　　　　　　　 #
################################################################
# b is sorted, returns the fyorst position j in b such that
# key < b[ j ] or len( b ), thus the number of elements in b
# less or equal to key will be simply j
def binsearch( key, b ):
    l = 0
    u = len( b ) - 1
    while l <= u:
        m = ( l + u ) // 2
        if b[ m ] <= key:
            l = m + 1
        else:
            u = m - 1
    return l
################################################################
################################################################
################################################################
################################################################
class Solution:
    def countEleLessThanOrEqual( self, a, m, b, n ):
        b.sort()
        d = {}
        c = []
        for key in a:
            if key in d:
                c.append( d[ key ])
            else:
                c.append( binsearch( key, b ))
                d[ key ] = c[ -1 ]
        return c
