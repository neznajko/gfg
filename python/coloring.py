# Distinct Coloring#####################################
############################################################
# There is a row of N houses, each house can be painted with 
# one of the three colors: red, blue or green. The cost of##
# painting each house with a certain color is different.####
# You have to paint all the houses such that no two adjacent
# houses have the same color. Find the minimum cost to paint 
# all houses. The cost of painting each house in red, blue##
# or green colour is given in the array r[], g[] and b[]####
# respectively.#############################################
########################################################
# Input: N = 3 r[] = {1, 1, 1} Output: 4 ###############
#              g[] = {2, 2, 2} #########################
#              b[] = {3, 3, 3} #########################
########################################################
# Expected Time Complexity: O(N) #######################
# Expected Auxiliary Space: O(N) #######################
########################################################
# Constraints: #########################################
# 1 <= r[i], g[i], b[i] ################################
########################################################
########################################################
########################################################
########################################################
########################################################
########################################################
########################################################
########################################################
########################################################
########################################################
########################################################
class Solution:
    def distinctColoring( self, n, r, g, b ):
        for j in range( 1, n ):
            r[j] += min( g[j - 1], b[j - 1] )
            g[j] += min( r[j - 1], b[j - 1] )
            b[j] += min( r[j - 1], g[j - 1] )
        return min( r[-1], g[-1], b[-1] )
