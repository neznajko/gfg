# Length of the longest substring
################################################################
# Given a string S, find the length of the longest substring
# without repeating characters
################################################################
# Input: S = "geeksforgeeks" Output: 7
# Explanation: Longest substring is "eksforg"
################################################################
# Input: S = "abdefgabef" Output: 6
# Explanation: Longest substring are "abdefg" "bdefga" and
# "defgab"
################################################################
# Expected Time Complexity: O(|S|)
# Expected Auxiliary Space: O(K) where K is constant
################################################################
# 1 ≤ |S| ≤ 10^5
# It is guaranteed that all characters of the String S will be
# lowercase letters from 'a' to 'z'
################################################################
################################################################
################################################################
################################################################
################################################################
################################################################
class Solution:
    ''''''
    #       i
    # M a t t i n a, d = {t:3 i:4 n:5 a:6}
    # 0 1 2 3 4 5 6, m = 4
    #             j
    def longestUniqueSubsttr( self, s ):              
        d = {} # character -> offset
        i = 0  # neva beginze
        m = 0  # maximum len
        for j, ch in enumerate( s ):
            if ch in d:
                m = max( m, j - i )
                k = d[ ch ]
                while i <= k:
                    ch = s[ i ]
                    if ch in d:
                        del d[ ch ]
                    i += 1
            d[ ch ] = j
        return max( m, j + 1 - i )
