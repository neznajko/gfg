class Stack:
    ''' 1. 月曜日 に 英語 の クラス が ありますか。
    '''
    """    はい、あります。
    """
    """ 2. 火曜日 に コンピューター の クラス が ありますか。
    """
    '''    いいえ、ありません。
    '''
    def __init__( self, k ):
        self.k = k
        self.deq = deque([[ 'ø', 0 ]])
    ### 3. 木曜日 に フランス 語 の クラス が ありあすか。
    ###    はい、あります。
    def push( self, char ):
        back = self.deq[ -1 ]
        if back[ CHAR ] == char:
            if back[ CONT ] == self.k - 1:
                self.deq.pop()
            else:
                back[ CONT ] += 1
        else:
            self.deq.append([ char, 1 ])
    ### 4. 土曜日 に クラス が ありますか。
    ###    いいえ、ありません。
    def join( self ):
        self.deq.popleft()
        ls = []
        for y in self.deq:
            ls.extend( y[ CONT ] * [ y[ CHAR ]])
        return ''.join( ls )    
            
