from math import inf
########################################################
########################################################
########################################################
########################################################
X, Y = 0, 1
########################################################
########################################################
########################################################
########################################################
class Solution: 
    def mostBalloons( self, n, a ):
        balloon_tab = {}
        for x, y in a:
            if( x, y ) in balloon_tab:
                balloon_tab[ x, y ] += 1
            else:
                balloon_tab[ x, y ] = 1
        #
        points = list( balloon_tab.keys() )
        n = len( points )
        m = 0
        for i in range( n ):
            src = points[i]
            d = {}
            for j in range( n ):
                if i == j: continue
                dst = points[j]
                dx = dst[X] - src[X]
                dy = dst[Y] - src[Y]
                if dy == 0:
                    f = inf
                else:
                    f = dx / dy
                if f in d:
                    d[f] += balloon_tab[dst]
                else:
                    d[f] =( balloon_tab[src] +
                            balloon_tab[dst] )
                m = max( m, d[f] )
        return m
