class Solution:
    def asteroidCollision( self, n, asteroids ):
        stk = [ asteroids[ 0 ]]
        j = 1
        while j < n:
            if stk and will_Collide( stk[ -1 ], asteroids[ j ]):
                c = com( stk[ -1 ], asteroids[ j ])
                if c <= 0: j += 1
                if c >= 0: stk.pop()
            else:
                stk.append( asteroids[ j ])
                j += 1            
        return stk
###############################################################_
################################################################
def will_Collide( w, y ):
    if w > 0: return y < 0
    return False
################################################################
################################################################
def com( a, b ):
    a = abs( a )
    b = abs( b )
    if a > b: return -1
    if a < b: return  1
    return 0
