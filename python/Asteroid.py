# We are given an integer array asteroids of size N representing 
# asteroids in a row. For each asteroid, the absolute value represents 
# its size, and the sign represents its direction (positive meaning 
# right, negative meaning left). Each asteroid moves at the same 
# speed. Find out the state of the asteroids after all collisions. 
# If two asteroids meet, the smaller one will explode. If both are 
# of same size, both will explode. Two asteroids moving in the same 
# direction will never meet.
################################################################
# Input: N = 3, asteroids[] = { 3, 5, -3 }
# Output: { 3, 5 }
# Explanation: The asteroid 5 and -3 collide resulting in 5. The 
# 5 and 3 never collide.
################################################################
