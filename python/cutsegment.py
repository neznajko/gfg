# Maximize The Cut Segments
################################################################
# Given an integer N denoting the Length of a line segment. You
# need to cut the line segment in such a way that the cut length
# of a line segment each time is either x , y or z. Here x, y,
# and z are integers.  After performing all the cut operations,
# your total number of cut segments must be maximum.
################################################################
# Input: N = 5, x = 5, y = 3, z = 2
# Output: 2
# Explanation: Here total length is 5, and the cut lengths are
# 5, 3 and 2. We can make two segments of lengths 3 and 2.
################################################################
# Expected Time Complexity: O(N)
# Expected Auxiliary Space: O(N)
################################################################
# 1 <= N, x, y, z <= 10^4
################################################################
################################################################
################################################################
################################################################
class Solution:
    ''''''
    def geta( self, j ):
        if j < 0: return 0
        return self.a[ j ]
    
    def maximizeTheCuts( self, n, x, y, z ):
        self.a = [ 0 ]*( n + 1 )
        if x <= n: self.a[ x ] = 1
        if y <= n: self.a[ y ] = 1
        if z <= n: self.a[ z ] = 1
        for j in range( 1, n + 1 ):
            m = max( self.geta( j - x ),
                     self.geta( j - y ),
                     self.geta( j - z ))
            if m > 0: m += 1
            self.a[ j ] = max( self.a[ j ], m )
        return self.a[ n ]
    
