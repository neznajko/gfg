# Minimum sum
################################################################
# Given an array Arr of size N such that each element is from
# the range 0 to 9. Find the minimum possible sum of two numbers
# formed using the elements of the array. All digits in the
# given array must be used to form the two numbers
################################################################
# Input: N = 6 Arr[] = {6, 8, 4, 5, 2, 3} Output: 604
# Explanation: The minimum sum is formed by numbers 358 and 246
################################################################
# Input: N = 5 Arr[] = {5, 3, 0, 7, 4} Output: 82
# Explanation: The minimum sum is formed by numbers 35 and 047
################################################################
# Expected Time Complexity: O( N * logN )
# Expected Auxiliary Space: O( N        )
################################################################
# 1 ≤ N ≤ 10^7
# 0 ≤ Arr[i] ≤ 9
################################################################
class Solution:
    def solve( self, a, n ):
        n = 0
        f = 1
        if len( a ) & 1: a.append( 0 )
        a.sort()
        j = len( a ) - 2
        while j >= 0:
            n += ( a[ j ] + a[ j + 1 ]) * f
            f *= 10
            j -= 2
        return n
