# Floor in BST
########################################################
# You are given a BST(Binary Search Tree) with n number of
# nodes and value x. your task is to find the greatest value
# node of the BST which is smaller than or equal to x.
# Note: when x is smaller than the smallest node of BST then
# returns -1.
########################################################
# Input: n = 7, 2         
#                \
#                81
#               /  \
#              42   87
#              \     \
#              66     90, x = 87
#              /
#            45
# Output: 87
# Explanation: ...
########################################################
# Expected Time Complexity: O(n)
# Expected Space Complexity: O(1)
########################################################
#                                               7 9 11 2
# y = 1              7
#     8          2``` ```9
#                         ``11
########################################################
class Solution:
    def floor( self, root, y ):
        f = -1
        while root:
            if root.data == y: return y
            if root.data  > y:
                root = root.left
            else:
                f = root.data
                root = root.right
        return f
########################################################
class Node: ############################################
    def __init__(self, val): ###########################
        self.right = None ##############################
        self.data = val ################################
        self.left = None ###############################
########################################################
def insert(tree, val): #################################
    if(tree==None): ####################################
        return Node(val) ###############################
    if(val< tree.data): ################################
        tree.left= insert(tree.left, val) ##############
    elif(val > tree.data): #############################
        tree.right= insert(tree.right, val) ############
    return tree ########################################
