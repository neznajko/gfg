def reverse( L ):                    ##
    prev = None                    ##  ##
    node = L                     ##  //  ##
    while node:                ##  //  //  ##
        _next = node.next    ##  //  ..  //  ##
        node.next = prev   ##  //  ..  ..  //  ##
        prev = node      ##  //  ..  ==  ..  //  ##
        node = _next   ##  //  ..  ==  ==  ..  //  ##
    return prev      ##  //  ..  ==  oo  ==  ..  //  ##
