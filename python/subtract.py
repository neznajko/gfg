def cmp( L1, L2 ):
    '''
    heute - today
    der Geburtstag - birthday
    '''
    len1 = getlen( L1 )
    len2 = getlen( L2 )
    if len1 > len2: return 1
    if len2 > len1: return 2
    while L1:
        if L1.data > L2.data: return 1
        if L1.data < L2.data: return 2
        L1 = L1.next
        L2 = L2.next
    return 0
