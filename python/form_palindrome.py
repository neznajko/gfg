# Form a palindrome
################################################################
# Given a string, find the minimum number of characters to be
# inserted to convert it to a palindrome string.
# 
# Examples :
# Input: str = "abcd"
# Output: 3
# Explanation: Inserted character marked with bold characters in
# dcbabcd, here we need minimum three characters to make it
# palindrome.
# Input: str = "aa"
# Output: 0
# Explanation: "aa" is already a palindrome.
# Constraints:
# 1 ≤ |str| ≤ 500
# str contains only lowercase alphabets.
################################################################
################################################################
################################################################
################################################################
################################################################
################################################################
# f( s[i..j] ):
#   if i = j: return 0
#   if s[i] = s[j]: return f( s[i + 1..j - 1] )
#   match_j = f( s[i..j - 1] )
#   match_i = f( s[i + 1..j] )
#   return min( match_i, match_j )
################################################################
################################################################
################################################################
################################################################
class Solution:
    def countMin( self, s ):
        n = len( s )
        f = [[0] * n for _ in range( n )]
        print( s )
        for window in range( 1, n ):
            for i in range( 0, n - window ):
                j = i + window
                if s[i] == s[j]:
                    f[i][j] = f[i + 1][j - 1]
                else:
                    f[i][j] = 1 + min( f[i + 1][j], f[i][j - 1])
        return f[0][n - 1]
