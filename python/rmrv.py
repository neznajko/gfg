# Remove and Reverse
########################################################
########################################################
########################################################
########################################################
# Given a string S which consists of only lowercase English
# alphabets, you have to perform the below operations: If
# the string S contains any repeating character, remove the
# first repeating character and reverse the string and again
# perform the above operation on the modified string, otherwise,
# you stop. You have to find the final string.
########################################################
########################################################
########################################################
########################################################
# Input: S = "abab" Output: ba
# Explanation: ...
########################################################
# Expected Time Complexity: O(|S|)
# Expected Auxiliary Space: O(K), K <= 26.
########################################################
# The string contains only lowercase English alphabets.
########################################################
########################################################
from collections import defaultdict
########################################################
########################################################
########################################################
########################################################
def get_tab( s ):
    tab = defaultdict( int )
    for c in s:
        tab[c] += 1
    return tab
########################################################
########################################################
########################################################
########################################################
#
#       -      [ h o ]
# h o a b a p
#     *        [ p a b ]
#
# [ p a b o h ]
#
class Solution:
    def removeReverse( self, s ):
        off = [ 0, len( s ) - 1 ]
        inc = 1, -1
        stk = [], []
        tab = get_tab( s )
        switch = False
        while off[0] <= off[1]:
            key = s[ off[ switch ]]
            off[ switch ] += inc[ switch ]
            if tab[key] > 1:
                tab[key] -= 1
                switch = not switch                
            else:
                stk[switch].append( key )
        src = stk[ not switch ]
        dst = stk[ switch ]
        while src:
            dst.append( src.pop())
        return "".join( dst )
########################################################
########################################################
########################################################
########################################################
if __name__ == '__main__': 
    t = int(input())
    for _ in range(t):
        S = input()
        ob = Solution()
        ans = ob.removeReverse(S)
        print(ans)
