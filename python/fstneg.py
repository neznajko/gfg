def main(): ####################################################
    T=int(input()) #############################################
    while(T>0): ################################################
        n=int(input()) #########################################
        a=[int(x) for x in input().strip().split()] ############
        k=int(input()) #########################################
        answer=printFirstNegativeInteger(a,n,k) ################
        for i in answer:print(i,end=" ") #######################
        print() ################################################
        T-=1 ###################################################
