# First negative integer in every window of size k
################################################################
# Given an array A[] of size N and a positive integer K, find
# the first negative integer for each and every window
# (contiguous subarray) of size K
################################################################
# Input: N = 5 A[] = {-8, 2, 3, -6, 10} K = 2
# Output: -8 0 -6 -6
# Explanation: First negative integer for each window of size k
# {-8, 2} = -8
# {2, 3} = 0 (does not contain a negative integer)
# {3, -6} = -6
# {-6, 10} = -6
################################################################
# Expected Time Complexity: O(N)
# Expected Auxiliary Space: O(K)
################################################################
# 1 <= N <= 10^5
# -10^5 <= A[i] <= 10^5
# 1 <= K <= N
################################################################
from collections import deque
################################################################
#           0   1 2   3    4 5
# n: 5, a: 48 974 5   0 -451 0, k: 3
#                     j
# start:    ^ 
# end:            ^
# deq( 0, -59, -59 )
def printFirstNegativeInteger( a, n, k ):
    ''''''
    deq = deque()
    a.append( 0 )
    
    j = n - k
    while j < n and a[j] >= 0: j += 1
    
    start = n - k
    end = start + k - 1
    while start >= 0:
        if a[ start ] < 0: j = start
        deq.appendleft( a[j])
        start -= 1
        end -=1
        if end < j: a[j] = 0
        
    return deq
