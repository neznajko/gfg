function divpairs( a, k ){
    const d = Array.from({ length: k }, () => 0 );
    let n = 0;
    a.forEach( y => { ++d[ y % k ]; });
    a.forEach( y => {
        const mod = y % k;
        --d[ mod ];
        n += d[( k - mod ) % k ];
    });
    return n;
}
////////////////////////////////////////////////////////////////
class Solution{
    countKdivPairs( a, n, k ){
        return divpairs( a, k );
    }
}
